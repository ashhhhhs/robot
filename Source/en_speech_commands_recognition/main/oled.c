static esp_err_t ssd1306_write_command(uint8_t command)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    if (!cmd) {
        ESP_LOGE(TAG, "Failed to create i2c command link");
        return ESP_ERR_NO_MEM;
    }
    
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (OLED_ADDRESS << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, 0x00, true);
    i2c_master_write_byte(cmd, command, true);
    i2c_master_stop(cmd);
    
    esp_err_t ret = i2c_master_cmd_begin(OLED_I2C_PORT, cmd, pdMS_TO_TICKS(1000));
    i2c_cmd_link_delete(cmd);
    
    if (ret != ESP_OK) {
        ESP_LOGD(TAG, "I2C write_command failed: 0x%x", ret);
    }
    
    return ret;
}

static esp_err_t ssd1306_write_data(const uint8_t *data, size_t size)
{
    if (!data || size == 0) {
        ESP_LOGE(TAG, "Invalid data pointer or size");
        return ESP_ERR_INVALID_ARG;
    }
    
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    if (!cmd) {
        ESP_LOGE(TAG, "Failed to create i2c command link");
        return ESP_ERR_NO_MEM;
    }
    
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (OLED_ADDRESS << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, 0x40, true);
    i2c_master_write(cmd, data, size, true);
    i2c_master_stop(cmd);
    
    esp_err_t ret = i2c_master_cmd_begin(OLED_I2C_PORT, cmd, pdMS_TO_TICKS(1000));
    i2c_cmd_link_delete(cmd);
    
    if (ret != ESP_OK) {
        ESP_LOGD(TAG, "I2C write_data failed: 0x%x", ret);
    }
    
    return ret;
}

static void ssd1306_send_command_list(const uint8_t *commands, size_t length)
{
    if (!commands || length == 0) {
        ESP_LOGE(TAG, "Invalid command list");
        return;
    }
    
    for (size_t i = 0; i < length; i++) {
        esp_err_t ret = ssd1306_write_command(commands[i]);
        if (ret != ESP_OK) {
            ESP_LOGW(TAG, "Failed to write command at index %d: 0x%x", i, ret);
        }
    }
}

static void oled_refresh_buffer(void)
{
    for (uint8_t page = 0; page < 8; page++) {
        uint8_t cmd[3] = { (uint8_t)(0xB0 | page), 0x00, 0x10 };
        ssd1306_send_command_list(cmd, sizeof(cmd));
        
        esp_err_t ret = ssd1306_write_data(&s_oled_buffer[page * 128], 128);
        if (ret != ESP_OK) {
            ESP_LOGW(TAG, "Failed to refresh page %d: 0x%x", page, ret);
        }
    }
}

static void oled_draw_char(uint8_t x, uint8_t page, char c)
{
    if (c < 32 || c > 126) {
        c = '?';
    }
    uint32_t index = (c - 32) * 5;
    if (x >= 124 || page >= 8) {
        return;
    }
    for (uint8_t i = 0; i < 5; i++) {
        s_oled_buffer[page * 128 + x + i] = ssd1306_font5x7[index + i];
    }
    if (x + 5 < 128) {
        s_oled_buffer[page * 128 + x + 5] = 0x00;
    }
}

static void oled_draw_text(uint8_t x, uint8_t page, const char *text)
{
    while (*text && x < 128) {
        oled_draw_char(x, page, *text);
        x += 6;
        text++;
    }
}

void oled_init(void)
{
    i2c_config_t i2c_cfg = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = OLED_I2C_SDA_GPIO,
        .scl_io_num = OLED_I2C_SCL_GPIO,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
        .master.clk_speed = 400000,
    };
    
    esp_err_t ret = i2c_param_config(OLED_I2C_PORT, &i2c_cfg);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "i2c_param_config failed: 0x%x", ret);
        return;
    }
    
    ret = i2c_driver_install(OLED_I2C_PORT, i2c_cfg.mode, 0, 0, 0);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "i2c_driver_install failed: 0x%x", ret);
        return;
    }

    static const uint8_t init_cmds[] = {
        0xAE, // display off
        0x20, 0x00, // memory addressing mode: horizontal
        0xB0, // page start address
        0xC8, // COM output scan direction
        0x00, // low column address
        0x10, // high column address
        0x40, // start line address
        0x81, 0x7F, // contrast
        0xA1, // seg remap
        0xA6, // normal display
        0xA8, 0x3F, // multiplex ratio
        0xA4, // display follows RAM content
        0xD3, 0x00, // display offset
        0xD5, 0x80, // display clock divide ratio/oscillator freq
        0xD9, 0x22, // pre-charge period
        0xDA, 0x12, // com pins hardware config
        0xDB, 0x20, // vcomh deselect level
        0x8D, 0x14, // enable charge pump
        0xAF  // display on
    };
    
    ssd1306_send_command_list(init_cmds, sizeof(init_cmds));
    vTaskDelay(pdMS_TO_TICKS(100));  // Wait for display to initialize
    
    oled_clear();
    oled_refresh_buffer();
    
    ESP_LOGI(TAG, "OLED initialized successfully");
}

void oled_clear(void)
{
    memset(s_oled_buffer, 0, sizeof(s_oled_buffer));
}

void oled_display_text(const char *line1, const char *line2)
{
    if (!line1 || !line2) {
        ESP_LOGW(TAG, "Invalid text pointers");
        return;
    }
    
    oled_clear();
    oled_draw_text(0, 0, line1);
    oled_draw_text(0, 2, line2);
    oled_refresh_buffer();
}
